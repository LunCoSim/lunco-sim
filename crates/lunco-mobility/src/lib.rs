/// Drive a rover forward/backward with optional steering.
///
/// `seq`/`tick` carry the client's per-vessel input sequence number + the
/// `SimTick` it was sampled at, for prediction reconciliation (the server echoes
/// the last-applied `seq` in snapshots; the client replays unacked inputs).
/// Field-level `default` keeps the HTTP API + older peers working when they omit
/// them (`DriveRover` can't derive `Default` — `Entity` has none).
#[Command]
pub struct DriveRover {
    #[authz_target]
    pub target: Entity,
    pub forward: f64,
    pub steer: f64,
    #[serde(default)]
    #[reflect(default)]
    pub seq: u32,
    #[serde(default)]
    #[reflect(default)]
    pub tick: u64,
}

/// Apply brakes to a rover. `seq`/`tick` as in [`DriveRover`] (paired input frame
/// shares the same `seq`).
#[Command]
pub struct BrakeRover {
    #[authz_target]
    pub target: Entity,
    pub intensity: f64,
    #[serde(default)]
    #[reflect(default)]
    pub seq: u32,
    #[serde(default)]
    #[reflect(default)]
    pub tick: u64,
}

// ── Command Observers ───────────────────────────────────────────────────────────

/// Observer for DriveRover commands.
#[on_command(DriveRover)]
fn on_drive_rover(
    cmd: DriveRover,
    mut q_rovers: Query<&mut FlightSoftware, With<RoverVessel>>,
    q_diff: Query<&DifferentialDrive>,
    q_ack: Query<&AckermannSteer>,
    mut q_digital_ports: Query<&mut DigitalPort>,
) {
    let Ok(fsw) = q_rovers.get_mut(cmd.target) else { return };

    if fsw.brake_active {
        for name in ["drive_left", "drive_right", "steering"] {
            if let Some(&port_id) = fsw.port_map.get(name) {
                if let Ok(mut p) = q_digital_ports.get_mut(port_id) { p.raw_value = 0; }
            }
        }
        return;
    }

    if let Ok(diff) = q_diff.get(cmd.target) {
        let left_mix = ((cmd.forward + cmd.steer) * 32767.0).clamp(-32767.0, 32767.0) as i16;
        let right_mix = ((cmd.forward - cmd.steer) * 32767.0).clamp(-32767.0, 32767.0) as i16;
        if let Some(&port_l) = fsw.port_map.get(&diff.left_port) {
            if let Ok(mut p) = q_digital_ports.get_mut(port_l) { p.raw_value = left_mix; }
        }
        if let Some(&port_r) = fsw.port_map.get(&diff.right_port) {
            if let Ok(mut p) = q_digital_ports.get_mut(port_r) { p.raw_value = right_mix; }
        }
    } else if let Ok(ack) = q_ack.get(cmd.target) {
        // Ackermann normally yaws the front wheels via a steering joint
        // — a `PhysicsRevoluteJoint` about Y between chassis and a
        // steering knuckle. We don't author those today, so the steer
        // port has no physical consumer on joint-based rovers. Until
        // those steering joints land, mix the steer command into the
        // left/right drive torques as a soft differential so the rover
        // can still turn under a `steer` command. The dedicated steer
        // port is still set for any consumer that implements true
        // Ackermann (raycast wheels render a visible steer angle from
        // it).
        // Skid-style mix at full magnitude until proper steering
        // knuckle joints (RevoluteJoint about Y between chassis and a
        // knuckle, with the wheel's X-axis hinge below the knuckle)
        // are authored. With the full mix the rover yaws under steer
        // input by spinning the L/R wheel groups in opposite
        // directions — visually identical to skid steer.
        let left_mix = ((cmd.forward + cmd.steer) * 32767.0).clamp(-32767.0, 32767.0) as i16;
        let right_mix = ((cmd.forward - cmd.steer) * 32767.0).clamp(-32767.0, 32767.0) as i16;
        let steer_val = (cmd.steer * 32767.0).clamp(-32767.0, 32767.0) as i16;
        if let Some(&port_l) = fsw.port_map.get(&ack.drive_left_port) {
            if let Ok(mut p) = q_digital_ports.get_mut(port_l) { p.raw_value = left_mix; }
        }
        if let Some(&port_r) = fsw.port_map.get(&ack.drive_right_port) {
            if let Ok(mut p) = q_digital_ports.get_mut(port_r) { p.raw_value = right_mix; }
        }
        if let Some(&port_s) = fsw.port_map.get(&ack.steer_port) {
            if let Ok(mut p) = q_digital_ports.get_mut(port_s) { p.raw_value = steer_val; }
        }
    }
}

/// Observer for BrakeRover commands.
#[on_command(BrakeRover)]
fn on_brake_rover(
    cmd: BrakeRover,
    mut q_rovers: Query<&mut FlightSoftware, With<RoverVessel>>,
    mut q_digital_ports: Query<&mut DigitalPort>,
) {
    let Ok(mut fsw) = q_rovers.get_mut(cmd.target) else { return };

    fsw.brake_active = cmd.intensity > 0.5;
    let port_val = if fsw.brake_active { 32767 } else { 0 };

    if let Some(&port_b) = fsw.port_map.get("brake") {
        if let Ok(mut p) = q_digital_ports.get_mut(port_b) { p.raw_value = port_val; }
    }
    if fsw.brake_active {
        for name in ["drive_left", "drive_right"] {
            if let Some(&port_id) = fsw.port_map.get(name) {
                if let Ok(mut port) = q_digital_ports.get_mut(port_id) { port.raw_value = 0; }
            }
        }
    }
}

