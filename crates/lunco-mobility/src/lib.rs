/// TEMP DIAGNOSTIC: log the accumulated per-chassis wheel stats once per tick.
/// `nspread` = max−min normal force (weight transfer / suspension ripple);
/// `long`/`lat` = peak slip components; `sat` = wheels at the friction cone;
/// `fricYaw` = net friction yaw torque (does it reverse tick-to-tick?).
fn debug_log_wheel_drive(
    mut dbg: ResMut<WheelDriveDebug>,
    q: Query<(Entity, Option<&Name>, &AngularVelocity, &LinearVelocity), With<RoverVessel>>,
) {
    for (e, name, av, lv) in q.iter() {
        if let Some(d) = dbg.0.get(&e) {
            if av.0.length_squared() > 1e-6 || lv.0.length_squared() > 1e-4 {
                info!(
                    "WD rover='{}' yaw={:+.5} v={:.3} nspread={:.1} long={:.3} lat={:.3} sat={}/{} fricYaw={:+.4}",
                    name.map(|n| n.as_str()).unwrap_or("?"),
                    av.0.y,
                    lv.0.length(),
                    d.n_max - d.n_min,
                    d.long_max,
                    d.lat_max,
                    d.sat,
                    d.wheels,
                    d.fric_yaw_torque,
                );
            }
        }
    }
    dbg.0.clear();
}

/// System applying longitudinal drive torque and lateral friction.
///
/// **Theory**: Drive force is applied along the wheel's forward vector at the
/// world-space contact point. Both longitudinal (forward/back) and lateral
/// (side-to-side) friction are computed using a Coulomb friction model where
/// the maximum friction force is `mu * normal_force`. This prevents the rover
/// from sliding like it's on ice and limits drive force to what the tire can
/// actually grip.
fn apply_wheel_drive(
    q_wheels: Query<(
        &WheelRaycast,
        &Transform,
        &RayHits,
        &ChildOf,
    )>,
    q_ports: Query<&lunco_core::architecture::PhysicalPort>,
    mut q_chassis: Query<(Forces, &RigidBody, Option<&FlightSoftware>), With<RoverVessel>>,
    mut dbg: ResMut<WheelDriveDebug>,
) {
    for (wheel, wheel_tf, hits, parent) in q_wheels.iter() {
        let parent_entity = parent.parent();
        if let Ok((mut forces, body, fsw)) = q_chassis.get_mut(parent_entity) {
            // Skip forces if body is kinematic
            if matches!(body, RigidBody::Kinematic) { continue; }
            // Braking: the wheel-spin model locks the spin, but the chassis only
            // stops if the contact grips. We make friction saturate (full cone)
            // while braking so a locked wheel actually decelerates the rover.
            let braking = fsw.is_some_and(|f| f.brake_active);

            if let Ok(port) = q_ports.get(wheel.drive_port) {
                // Traction only exists when the ray is hitting the ground
                if hits.iter().next().is_some() {
                    let normal_force = wheel.last_normal_force;
                    if normal_force < 1.0 {
                        // Not enough contact to transmit meaningful force
                        continue;
                    }

                    // Reconstruct the wheel's world pose in the AVIAN physics frame
                    // from the chassis Position/Rotation + the wheel's LOCAL transform
                    // (exactly as `apply_wheel_suspension` does). Using `GlobalTransform`
                    // here mixed the big_space floating-origin/render frame into avian's
                    // cell-local frame: `forces.apply_force_at_point` and the lever arm
                    // `hub - forces.position()` then used a point offset by the whole
                    // origin-rebasing distance, producing spurious torque/slip once the
                    // rover drove away from the floating origin (masked near it).
                    // `wheel_tf.rotation` carries the steer angle (set in
                    // `apply_wheel_steering`); roll-spin lives on the child visual, so the
                    // drive direction stays correct.
                    let wheel_world_rot = forces.rotation().0 * wheel_tf.rotation.as_dquat();
                    let hub_pos_world =
                        forces.position().0 + forces.rotation().0 * wheel_tf.translation.as_dvec3();
                    let forward: DVec3 = wheel_world_rot * DVec3::NEG_Z;
                    let right: DVec3 = wheel_world_rot * DVec3::X;

                    // --- Drive force ---
                    // Physical port value is already scaled by the wire gain.
                    // Treated as a normalized throttle in [-1, 1]: positive drives
                    // forward, NEGATIVE drives in reverse (the differential mix on
                    // `DigitalPort` already carries the sign; clamping to 0 here was
                    // what blocked reverse).
                    let throttle = (port.value as f64).clamp(-1.0, 1.0);
                    let drive_force_mag = throttle * normal_force * DEFAULT_DRIVE_FORCE_PER_NORMAL;
                    let drive_force_vec = forward * drive_force_mag;
                    forces.apply_force_at_point(drive_force_vec, hub_pos_world);

                    // --- Friction (longitudinal + lateral) ---
                    // Coulomb friction cone: total friction force magnitude <= mu * N.
                    // `friction_mu` is the tyre/ground grip (USD-authored, per wheel).
                    let max_friction = wheel.friction_mu * normal_force;

                    let chassis_vel = forces.linear_velocity();
                    let chassis_ang_vel = forces.angular_velocity();
                    let hub_vel = chassis_vel + chassis_ang_vel.cross(hub_pos_world - forces.position().0);

                    // Longitudinal slip velocity (how fast the wheel is spinning vs rolling)
                    let long_vel = hub_vel.dot(forward);
                    // Lateral slip velocity (sideways sliding)
                    let lat_vel = hub_vel.dot(right);

                    // Combined slip speed and direction
                    // Friction opposes the slip *velocity vector*. Expressing it on
                    // the vector (not a normalized direction × magnitude behind a
                    // slip dead-band) keeps it a continuous damper through zero slip:
                    // `-k·slip` in the linear regime, saturating at the Coulomb cone
                    // `μ·N`. The old `if slip_speed > MIN_SLIP_SPEED` gate left
                    // sub-threshold motion UNDAMPED, so a resting/gently-turning
                    // chassis drifted free until it crossed the threshold, got snapped
                    // back, and drifted again — a stiction limit-cycle that showed up
                    // as a tick-period yaw chatter (the steering jitter). Smooth to
                    // zero removes it; at this mass `-k·slip` is well overdamped so it
                    // cannot overshoot.
                    let slip_vec = long_vel * forward + lat_vel * right;
                    let slip_speed = slip_vec.length();
                    let friction_force = if braking && slip_speed > 1e-6 {
                        // Braking: a locked wheel grips at the full Coulomb cone,
                        // opposing all sliding — this is what actually stops the rover.
                        -slip_vec * (max_friction / slip_speed)
                    } else if slip_speed * wheel.contact_grip_stiffness <= max_friction {
                        // Linear grip — `-k·slip`, → 0 continuously as slip → 0
                        // (no division, safe at exactly zero slip).
                        -slip_vec * wheel.contact_grip_stiffness
                    } else {
                        // Saturated at the cone; slip_speed > 0 here.
                        -slip_vec * (max_friction / slip_speed)
                    };
                    forces.apply_force_at_point(friction_force, hub_pos_world);

                    // TEMP DIAGNOSTIC (forward+turn jitter localization): accumulate
                    // per-chassis wheel stats this tick. Remove with its resource,
                    // logging system, and the `dbg` param.
                    let lever = hub_pos_world - forces.position().0;
                    let d = dbg.0.entry(parent_entity).or_insert_with(ChassisDriveDbg::new);
                    d.n_min = d.n_min.min(normal_force);
                    d.n_max = d.n_max.max(normal_force);
                    d.long_max = d.long_max.max(long_vel.abs());
                    d.lat_max = d.lat_max.max(lat_vel.abs());
                    if slip_speed * wheel.contact_grip_stiffness > max_friction {
                        d.sat += 1;
                    }
                    d.wheels += 1;
                    d.fric_yaw_torque += lever.cross(friction_force).y;
                }
            }
        }
    }
}

/// Updates steering angle based on physical port state.
///
/// The steering port value (normalized -1 to 1) is mapped to a yaw rotation
/// around the local Y axis. For front wheels on Ackermann rovers, this
/// produces the steer angle. For non-steering wheels, the port is
/// `Entity::PLACEHOLDER` and this system skips them.
fn apply_wheel_steering(
    mut q_wheels: Query<(&WheelRaycast, &mut Transform, &ChildOf)>,
    q_ports: Query<&lunco_core::architecture::PhysicalPort>,
    q_chassis: Query<&RigidBody, With<RoverVessel>>,
    mut q_visual: Query<&mut Transform, Without<WheelRaycast>>,
) {
    for (wheel, mut transform, parent) in q_wheels.iter_mut() {
        // Predict-own: this chain now runs on a client too (the `!Client` gate
        // was dropped so the owned rover steers locally). Unlike the suspension
        // and drive systems this one writes wheel rotation directly with no
        // physics guard — so gate it on the chassis the same way: skip wheels of
        // a `Kinematic` chassis (every replicated rover this peer does NOT own),
        // whose local steer ports are stale and would point the wheels wrong.
        if let Ok(body) = q_chassis.get(parent.parent()) {
            if matches!(body, RigidBody::Kinematic) {
                continue;
            }
        }
        if wheel.steer_port == Entity::PLACEHOLDER {
            continue;
        }
        if let Ok(port) = q_ports.get(wheel.steer_port) {
            // Port value is -1.0 to 1.0; map to +/- 0.5 rad (~30 degrees)
            let target_angle = (port.value as f32).clamp(-1.0, 1.0) * 0.5;
            // Steer rotation is around local Y (up) axis. The visual mesh
            // rotation (steer + roll spin) is composed in `update_wheel_spin`.
            transform.rotation = Quat::from_rotation_y(target_angle);
        }
    }
}

/// Control Logic: Differential (Skid) mixing.
/// 
/// **Math**: `Left = Forward + Steer`, `Right = Forward - Steer`. 
/// Creates a torque differential that rotates the chassis.
#[derive(Component, Debug, Clone, Reflect, Default)]
#[reflect(Component, Default)]
pub struct DifferentialDrive {
    /// Digital port identifier for left-side motors.
    pub left_port: String,
    /// Digital port identifier for right-side motors.
    pub right_port: String,
}

/// Control Logic: Ackermann Steering.
/// 
/// **Math**: `Drive = Forward`, `SteerAngle = Input * MaxAngle`. 
/// High-stability steering for vehicles with articulating front/rear axles.
#[derive(Component, Debug, Clone, Reflect, Default)]
#[reflect(Component, Default)]
pub struct AckermannSteer {
    /// Port name for drive motors (left).
    pub drive_left_port: String,
    /// Port name for drive motors (right).
    pub drive_right_port: String,
    /// Port name for the steering servo.
    pub steer_port: String,
    /// Maximum steering lock angle (radians).
    pub max_steer_angle: f32,
}

/// Suspension configuration for joint-based (non-raycast) chassis.
///
/// **Why**: Some vehicles use physical collision wheels for higher fidelity, 
/// but still require emulated spring-damper logic for PrismaticJoints.
#[derive(Component, Debug, Clone, Reflect)]
#[reflect(Component, Default)]
pub struct Suspension {
    /// target static length of the strut.
    pub rest_length: f64,
    /// Stiffness (N/m).
    pub spring_k: f64,
    /// Dampening (Ns/m).
    pub damping_c: f64,
    /// Direction of extension.
    pub local_axis: DVec3,
}

impl Default for Suspension {
    fn default() -> Self {
        Self {
            rest_length: 0.4,
            spring_k: 50000.0,
            damping_c: 2000.0,
            local_axis: DVec3::Y,
        }
    }
}

/// Solves linear suspension equations for entities linked by joints.
///
/// **Model**: Spring-damper using Hooke's law applied along the prismatic
/// joint's slider axis. Damping is bidirectional — it resists both compression
/// and extension to prevent oscillation. The force is applied as an equal and
/// opposite pair on the two connected bodies.
fn suspension_system(
    q_joints: Query<(&PrismaticJoint, &Suspension)>,
    mut q_bodies: Query<Forces>,
) {
    for (joint, susp) in q_joints.iter() {
        let e1 = joint.body1;
        let e2 = joint.body2;

        if let Ok([mut forces1, mut forces2]) = q_bodies.get_many_mut([e1, e2]) {
            let pos1 = forces1.position().0;
            let rot1 = forces1.rotation().0;
            let pos2 = forces2.position().0;
            let rot2 = forces2.rotation().0;

            let world_axis: DVec3 = rot1 * susp.local_axis;

            let anchor1_world: DVec3 = pos1 + rot1 * joint.local_anchor1().unwrap_or_default();
            let anchor2_world: DVec3 = pos2 + rot2 * joint.local_anchor2().unwrap_or_default();

            let diff_world: DVec3 = anchor2_world - anchor1_world;
            let current_length: f64 = -diff_world.dot(world_axis);
            let vel1 = forces1.velocity_at_point(anchor1_world);
            let vel2 = forces2.velocity_at_point(anchor2_world);
            let rel_vel: f64 = (vel2 - vel1).dot(world_axis);

            let compression: f64 = (susp.rest_length - current_length).max(0.0);
            let spring_force_mag: f64 = compression * susp.spring_k;

            // Damping opposes relative motion: positive when compressing (adds
            // force), negative when extending (reduces force). Clamp total to
            // zero minimum so we never pull bodies together.
            let damping_force_mag: f64 = rel_vel * susp.damping_c;
            let total_force_mag: f64 = (spring_force_mag + damping_force_mag).clamp(0.0, 100_000.0);

            if !total_force_mag.is_finite() { continue; }

            let force_vec: DVec3 = world_axis * total_force_mag;

            forces1.apply_force_at_point(force_vec, anchor1_world);
            forces2.apply_force_at_point(-force_vec, anchor2_world);
        }
    }
}

