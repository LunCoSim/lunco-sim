/// Simple wrapper around rumoca-compile for compiling Modelica models.
///
/// MSL is preloaded into the session at construction time via
/// [`rumoca_compile::compile::Session::load_source_root_tolerant`].
/// After preload, compiling any MSL-based user model is a plain
/// strict-reachable-DAE call against a session that already has
/// every MSL class visible to rumoca's §5 scope walker.
///
/// Why preload instead of demand-load? Demand-load requires
/// rumoca to emit fully-qualified references in its unresolved-ref
/// diagnostics so an external source provider can act on them.
/// Upstream rumoca currently emits raw short forms (`SI.Time`,
/// `Continuous.Filter`, `Rotational.Interfaces.PartialTwoFlanges`)
/// without scope qualification — which means an external resolver
/// has no way to disambiguate. Preload sidesteps the issue: once
/// every MSL class is in the session, the scope walker never has
/// to ask outside.
///
/// Cost: first session construction blocks while the parsed-artifact
/// cache (bincode under `RUMOCA_CACHE_DIR`) is hit. With a warm
/// cache, MSL loads in ~2–5 s. Cold cache (first run after a rumoca
/// version bump) is proportional to parser throughput; `msl_indexer`
/// can pre-warm offline.
/// Cheap, side-effect-free check for whether a library set is installable
/// on this target — used to gate the demand-driven MSL install so we never
/// run a redundant strict pass / install attempt when there's nothing to
/// install. Does NOT decode the bundle (that's the expensive part): it only
/// looks for an already-decoded process-wide slot, a native MSL filesystem
/// root, or a web in-memory source.
fn msl_is_available() -> bool {
    msl_remote::global_parsed_msl().is_some()
        || lunco_assets::msl::primary_filesystem_root().is_some()
        || lunco_assets::msl::has_in_memory_source()
        // On-disk tree even if not registered as a global source. The app
        // registers MSL at startup (so `primary_filesystem_root` is set), but
        // headless contexts (unit tests, the indexer, a fresh embedder) only
        // have the files on disk — demand-driven load must still find them.
        || lunco_assets::msl_source_root_path().is_some()
}

pub struct ModelicaCompiler {
    session: Session,
    /// Whether the library set (MSL + companions + discovered extra libs)
    /// has been installed into this session yet. Layer-A demand-driven gate:
    /// MSL is installed lazily by [`Self::compile_loaded`] on the first
    /// compile whose failure rumoca attributes to an unresolved reference
    /// (`ER002`) — never at construction. A library-free model never sets
    /// this and never pays the MSL cost. Latches `true` after one install
    /// so subsequent compiles skip the gate entirely.
    msl_installed: bool,
    /// Whether the shipped `LunCo` Modelica library (`assets/models/LunCo`, a
    /// standard structured package) has been seated into this session. Same
    /// demand-driven gate as [`Self::msl_installed`], but the library is tiny and
    /// always embedded, so the install is a handful of in-memory documents rather
    /// than MSL's 316 MB bundle. Seated on the first compile whose failure rumoca
    /// attributes to an unresolved reference — e.g. a rover's `rucheyok_electrical.mo`
    /// referencing `LunCo.Electrical.Battery`.
    lunco_installed: bool,
    /// URIs of the user documents currently seated as overlays in this
    /// reused session (NOT the MSL/library source roots). Every compile is
    /// HERMETIC with respect to prior compiles: before seating its own
    /// document set, a compile evicts any previously-seated user doc that is
    /// not part of *this* compile. Without this, a prior compile's primary
    /// overlay stays resident, and compiling a SECOND document that defines
    /// the same package (e.g. the same model opened in two tabs / restored +
    /// shared) leaves the package registered under two URIs → rumoca's merge
    /// pass fails with `Duplicate class '…' found in '…' with non-identical
    /// definition`. The per-doc `session_uri` keying alone can't prevent this
    /// because the two docs legitimately have different URIs; the session
    /// itself must hold only the active compile's user docs.
    seated_user_uris: std::collections::HashSet<String>,
}

impl ModelicaCompiler {
    /// Construct a compiler and preload MSL.
    ///
    /// MSL discovery order:
    ///
    /// 1. The process-wide source from [`lunco_assets::msl::global_msl_source`]
    ///    if it's been installed. This is how the wasm runtime feeds the
    ///    fetched-from-server MSL bundle in.
    /// 2. Fall back to [`lunco_assets::msl_source_root_path`] (filesystem).
    ///
    /// If both are absent — typical for the first wasm tick before the
    /// MSL bundle has finished downloading — the session is left empty
    /// and the next compile will surface `unresolved type reference`
    /// diagnostics until MSL lands. Callers that want to gate compiles
    /// on MSL readiness should consult `MslLoadState`.
    pub fn new() -> Self {
        // Layer A: build an EMPTY session — MSL is no longer installed
        // eagerly here. A library-free model (Balloon, standalone) then
        // compiles against the empty session in a fraction of the time and
        // never pays the ~1.5s MSL decode+install nor the cost of resolving
        // against 2670 resident classes. MSL is installed on demand by
        // `compile_loaded` only when rumoca reports an unresolved reference
        // (`ER002`) that a library could satisfy — the decision comes from
        // rumoca's own structured diagnostics, not a source-text guess.
        let mut compiler = Self {
            session: Session::new(SessionConfig::default()),
            msl_installed: false,
            lunco_installed: false,
            seated_user_uris: std::collections::HashSet::new(),
        };
        // Escape hatch: `LUNCO_MODELICA_PRELOAD_MSL=1` forces the old eager
        // behaviour, e.g. to pre-pay the install before a latency-sensitive
        // interactive run. Default off — the demand-driven path covers it.
        let eager = std::env::var("LUNCO_MODELICA_PRELOAD_MSL")
            .map(|v| v != "0" && !v.is_empty())
            .unwrap_or(false);
        if eager {
            compiler.ensure_msl_installed();
        }
        compiler
    }

    /// Install the library set (MSL + companions + discovered extra libs)
    /// into this session, once. Returns `true` if classes are now resident
    /// (just installed, or already were). Idempotent and cheap on repeat.
    ///
    /// This is the demand-driven install hook. It reuses the same
    /// [`Self::preload_from_global`] machinery the old eager path used
    /// (process-wide decoded bundle → on-disk `parsed-msl.bin` → cold
    /// parse), so there is exactly one install code path. Callers that
    /// *know* they need the full library up front (the `msl_indexer --warm`
    /// pass) call this explicitly; `compile_loaded` calls it lazily on an
    /// unresolved-reference failure.
    pub fn ensure_msl_installed(&mut self) -> bool {
        if self.msl_installed {
            return true;
        }
        let t = web_time::Instant::now();
        if Self::preload_from_global(&mut self.session, t) {
            self.msl_installed = true;
        }
        self.msl_installed
    }

    /// Seat the shipped `LunCo` Modelica library into this session, once.
    ///
    /// The library is `assets/models/LunCo` — a standard structured package
    /// (`package.mo` + `package.order` + members), embedded at compile time by
    /// `lunco_assets::models` so it is present on every target including wasm. This
    /// loads it via [`Session`]'s in-memory source-root path, the same call bundled
    /// examples use; rumoca then resolves `import LunCo.Electrical` the standard way,
    /// from the packages' `within` declarations. Idempotent; cheap (a few small docs).
    pub fn ensure_lunco_installed(&mut self) -> bool {
        if self.lunco_installed {
            return true;
        }
        let files = lunco_assets::models::package_files("LunCo");
        if files.is_empty() {
            return false;
        }
        let _ = self.load_source_root_in_memory("LunCo", "LunCo", files);
        self.lunco_installed = true;
        true
    }

    /// If a process-wide MSL has been installed, preload it into the
    /// session. Returns `true` when handled (caller skips the disk
    /// fallback).
    ///
    /// Two web-side fast paths, in priority order:
    ///
    /// 1. **Pre-parsed bundle** ([`msl_remote::global_parsed_msl`]).
    ///    The chunked parse driver finished running, so we already have
    ///    `Vec<(uri, StoredDefinition)>` in hand — install via
    ///    `Session::replace_parsed_source_set` (no parsing). This is
    ///    the steady-state path on web.
    /// 2. **Source-only bundle** (`MslAssetSource::InMemory`). Bytes
    ///    are decompressed but the chunked parser hasn't finished yet.
    ///    Falling through to `load_source_root_in_memory` here would
    ///    block the main thread for ~60–120 s, so we skip preload and
    ///    let the caller see an empty session — the auto-retrigger on
    ///    parse-complete will rebuild the compiler properly.
    fn preload_from_global(session: &mut Session, t_total: web_time::Instant) -> bool {
        // 1. Slot hit, shared by native and wasm: the pre-parsed bundle is
        //    already in memory (wasm chunked-decoder output, or a prior
        //    native load that populated the process-wide slot). Cheap
        //    install, no disk. Kept slot-only on purpose so a non-MSL
        //    caller (sandbox/Balloon, `SkipMslAutoLoad`) never eagerly
        //    reads the on-disk bundle here — the native disk read lives in
        //    branch 3, behind the `Filesystem`-source gate.
        if let Some(parsed) = msl_remote::global_parsed_msl() {
            let docs = (**parsed).clone();
            let pair_count = docs.len();
            let inserted = session.replace_parsed_source_set(
                "msl",
                rumoca_compile::compile::SourceRootKind::DurableExternal,
                docs,
                None,
            );
            log::info!(
                "[ModelicaCompiler] installed pre-parsed MSL in {:.2}s: \
                 {} inserted (of {} docs)",
                t_total.elapsed().as_secs_f64(),
                inserted,
                pair_count,
            );
            return true;
        }
        if lunco_assets::msl::has_in_memory_source() {
            // wasm: the source bytes are resident but the chunked parser
            // hasn't produced `StoredDefinition`s yet. Report `false`
            // (nothing installed) so `ensure_msl_installed` does NOT latch —
            // a later compile retries once the chunked decode lands and
            // `global_parsed_msl()` fills (branch 1 above).
            log::info!(
                "[ModelicaCompiler] MSL bundle present but parse not yet complete — \
                 session stays empty; install retried when parse finishes"
            );
            return false;
        }
        if lunco_assets::msl::primary_filesystem_root().is_some()
            || lunco_assets::msl_source_root_path().is_some()
        {
            // Native: an MSL tree is present (registered as a global source,
            // OR just materialised on disk — headless tests/indexer/embedders
            // don't register one). The install paths below read the on-disk
            // bundle / source tree via `msl_dir()` directly, so disk presence
            // alone is enough. Try the pre-parsed on-disk bundle first via the
            // shared, memoized
            // `install_parsed_msl` path — it streams `parsed-msl.bin` and
            // caches it process-wide, so the next `ModelicaCompiler` hits
            // branch 1 above with no disk read. This is the exact same
            // install `load_source_root` uses: one code path, native + wasm.
            if let Some((total, inserted)) = Self::install_parsed_msl(session, "msl") {
                log::info!(
                    "[ModelicaCompiler] loaded pre-parsed MSL bundle in {:.2}s: \
                     {} inserted (of {} docs)",
                    t_total.elapsed().as_secs_f64(),
                    inserted,
                    total,
                );
                return true;
            }

            // No usable bundle (missing, or rumoca-version-stale and failed
            // to decode). Cold path: build the bundle the SAME way
            // `msl_indexer` does — `indexer::parse_native_msl_bundle` walks
            // every native root (MSL tree + companions + discovered extra
            // libs) and parses ONE FILE AT A TIME.
            //
            // We deliberately do NOT use rumoca's
            // `parse_source_root_with_cache_in`: it fires a single
            // all-files rayon batch, which spikes memory (up to `num_cpus`
            // concurrent ASTs + a full-tree blake3 hash sweep) and bursts
            // concurrent artifact-cache writes. Fine standalone, but here it
            // runs on the worker thread *alongside the Bevy render loop*, so
            // on weak (8 GB) machines the peak swaps and stalls the whole
            // desktop. Per-file keeps the peak flat — `msl_indexer` has
            // always parsed this way and finishes in minutes where the batch
            // path took tens of minutes. DRY: one MSL source→bundle parser
            // shared by the CLI and the workbench.
            #[cfg(not(target_arch = "wasm32"))]
            {
                let docs = crate::indexer::parse_native_msl_bundle();
                if docs.is_empty() {
                    log::warn!(
                        "[ModelicaCompiler] no MSL source files found under native \
                         roots; session starts empty"
                    );
                    return false;
                }
                let parsed_count = docs.len();
                // Write the bundle (zstd-compressed) BEFORE moving the docs
                // into the session so we don't clone ~165 MB of defs, and so
                // the next launch hits the fast path above (~1s).
                let bundle_path = lunco_assets::msl_dir().join("parsed-msl.bin");
                if let Some(parent) = bundle_path.parent() {
                    let _ = std::fs::create_dir_all(parent);
                }
                match msl_remote::write_parsed_bundle(&bundle_path, &docs) {
                    Ok(()) => log::info!(
                        "[ModelicaCompiler] wrote compressed MSL bundle ({} docs) to `{}` — next launch will be ~1s",
                        docs.len(),
                        bundle_path.display()
                    ),
                    Err(e) => log::warn!(
                        "[ModelicaCompiler] failed to write MSL bundle to `{}`: {e}",
                        bundle_path.display()
                    ),
                }
                let inserted = session.replace_parsed_source_set(
                    "msl",
                    rumoca_compile::compile::SourceRootKind::DurableExternal,
                    docs,
                    None,
                );
                log::info!(
                    "[ModelicaCompiler] preloaded MSL (cold, per-file) in {:.2}s: \
                     {} parsed / {} inserted",
                    t_total.elapsed().as_secs_f64(),
                    parsed_count,
                    inserted,
                );
                return true;
            }
            #[cfg(target_arch = "wasm32")]
            return false;
        }
        false
    }

    /// Install the process-wide pre-parsed MSL bundle into `session`
    /// under source-set `id`, if a bundle is available.
    ///
    /// **Single shared path for native and wasm.** The bundle is acquired
    /// through [`msl_remote::parsed_msl_bundle`], the one memoized
    /// accessor: on native it streams `parsed-msl.bin` off disk on the
    /// first call and caches it process-wide (so a second
    /// `ModelicaCompiler`, or a later [`Self::load_source_root`], reuses
    /// it with no extra read/decode); on wasm it returns the chunked
    /// decoder's slot. Both [`Self::preload_from_global`] and
    /// [`Self::load_source_root`] go through here, so a change to how MSL
    /// is sourced or installed lands on both targets at once.
    ///
    /// Returns `Some((total_docs, inserted))`, or `None` when no bundle is
    /// ready yet (wasm before the chunked parse finishes, or native before
    /// the indexer has written `parsed-msl.bin`).
    fn install_parsed_msl(session: &mut Session, id: &str) -> Option<(usize, usize)> {
        // The three real costs here are: decode `parsed-msl.bin`, deep-clone
        // the (~165MB) AST set off the Arc, and insert+invalidate in the
        // session. The clone dominates — see CQ-213 for eliminating it.
        let parsed = msl_remote::parsed_msl_bundle()?;
        let docs = (**parsed).clone();
        let total = docs.len();
        let inserted = session.replace_parsed_source_set(
            id,
            rumoca_compile::compile::SourceRootKind::DurableExternal,
            docs,
            None,
        );
        log::debug!("[install_parsed_msl] {total} docs, {inserted} inserted");
        Some((total, inserted))
    }

    /// Compile Modelica source string and return DAE result.
    ///
    /// The user source is fed as a workspace document on top of the
    /// already-preloaded MSL. Rumoca's strict-reachable DAE walker
    /// sees the user's model plus the entire MSL class tree, so
    /// short-form refs like `SI.Time`, `Continuous.Filter`, etc.
    /// resolve through normal MLS §5 scope lookup.
    ///
    /// `filename` is used as the document URI for error reporting.
    pub fn compile_str(
        &mut self,
        model_name: &str,
        source: &str,
        filename: &str,
    ) -> Result<Box<rumoca_compile::compile::DaeCompilationResult>, String> {
        let mut keep = std::collections::HashSet::new();
        keep.insert(filename.to_string());
        self.evict_user_docs_except(&keep);
        self.seat_user_source(filename, source);
        self.seated_user_uris = keep;
        self.compile_loaded(model_name)
    }

    /// Seat one user source into the session — **the single chokepoint where
    /// user model text enters rumoca**, and therefore the one place the
    /// bound-`input` workaround is applied.
    ///
    /// rumoca demotes a bound `input Real g = 9.81` to an algebraic, so it never
    /// reaches `input_names()` and `set_input("g", …)` fails
    /// (`docs/architecture/29-rumoca-workarounds.md` §2). Stripping here rather
    /// than at each caller means no compile path can forget it — `modelica_tester`
    /// silently did, and every future entry point would have been one `git grep`
    /// away from the same bug.
    ///
    /// Safe to apply to already-stripped source: [`strip_input_defaults`] blanks
    /// the binding bytes in place, so a second pass finds nothing to strip and is
    /// a no-op. Callers that need the defaults back (the worker, to re-seed them
    /// via `set_input`) still call it themselves — they get the same answer.
    ///
    /// The blanking is LENGTH-PRESERVING, so byte offsets into the raw source the
    /// caller still holds (diagnostic spans → editor click-to-source) keep
    /// pointing at the same characters.
    fn seat_user_source(&mut self, filename: &str, source: &str) {
        let (stripped, _defaults) = crate::ast_extract::strip_input_defaults(source);
        self.session.update_document(filename, &stripped);
    }

    /// Remove every previously-seated user-document overlay whose URI is not
    /// in `keep`, so the reused session holds ONLY the active compile's user
    /// docs (plus the immutable MSL/library source roots). This is what makes
    /// each compile hermetic against prior compiles — see
    /// [`Self::seated_user_uris`]. MSL roots are installed via
    /// `replace_parsed_source_set` (not tracked here), so they're untouched.
    fn evict_user_docs_except(&mut self, keep: &std::collections::HashSet<String>) {
        let stale: Vec<String> = self
            .seated_user_uris
            .iter()
            .filter(|uri| !keep.contains(*uri))
            .cloned()
            .collect();
        for uri in stale {
            self.session.remove_document(&uri);
            self.seated_user_uris.remove(&uri);
        }
    }

    /// Recompute **structured, located** diagnostics for a model that
    /// just failed [`Self::compile_str`].
    ///
    /// Where `compile_str`'s `Err(String)` is a flat human summary,
    /// this returns one [`Diagnostic`](lunco_doc::Diagnostic) per
    /// rumoca failure — each prefixed with the diagnostic code
    /// (e.g. `[ED008]`) and, when the failure's primary span points
    /// into the user's `model.mo`, carrying a 1-based (line, column)
    /// so the Diagnostics panel can make the row click-to-source
    /// (the same treatment parse + lint findings already get).
    ///
    /// Runs rumoca's report-returning strict compile
    /// (`compile_model_strict_reachable_uncached_with_recovery`),
    /// which reuses the session's already-resolved AST/source — so
    /// this is a cheap second pass, taken only on the error path.
    /// `user_uri` is the document URI the caller passed to
    /// `compile_str` (`"model.mo"` for the workbench); only spans in
    /// that file are made clickable.
    pub fn compile_diagnostics(
        &mut self,
        model_name: &str,
        user_uri: &str,
    ) -> Vec<lunco_doc::Diagnostic> {
        let report = self
            .session
            .compile_model_strict_reachable_uncached_with_recovery(model_name);
        diagnostics_from_strict_report(&report, user_uri)
    }

    /// Like `compile_str`, but seats additional `(filename, source)`
    /// pairs into the rumoca session before compiling so the resolver
    /// can satisfy cross-doc class references (e.g. a fresh untitled
    /// `RocketStage` referencing `AnnotatedRocketStage.Tank` from a
    /// sibling untitled doc that holds the package). Each extra is
    /// loaded via the same `update_document` path; rumoca dedups by
    /// filename so re-loading the same file is harmless.
    ///
    /// The active compile's user docs (primary + extras) become the session's
    /// ENTIRE user-overlay set: any previously-seated user doc that isn't part
    /// of this compile is evicted first (see [`Self::seated_user_uris`] /
    /// [`Self::evict_user_docs_except`]). This keeps the reused session
    /// hermetic — a prior compile of the same package under a different URI
    /// (e.g. the same model open in two tabs) can't linger and trip rumoca's
    /// `Duplicate class '…' found in '…' with non-identical definition`. The
    /// seated docs are left resident on return (the error-path
    /// `compile_diagnostics` re-reads them); they're pruned by the NEXT
    /// compile, not removed here.
    pub fn compile_str_multi(
        &mut self,
        model_name: &str,
        source: &str,
        filename: &str,
        extras: &[(String, String)],
    ) -> Result<Box<rumoca_compile::compile::DaeCompilationResult>, String> {
        let mut keep = std::collections::HashSet::new();
        keep.insert(filename.to_string());
        for (extra_filename, _) in extras {
            if extra_filename != filename {
                keep.insert(extra_filename.clone());
            }
        }
        self.evict_user_docs_except(&keep);
        for (extra_filename, extra_source) in extras {
            if extra_filename == filename {
                continue;
            }
            self.seat_user_source(extra_filename, extra_source);
        }
        self.seat_user_source(filename, source);
        self.seated_user_uris = keep;
        self.compile_loaded(model_name)
    }

    /// Compile an MSL class that is already loaded into the session
    /// (no `update_document` call). Used by the `msl_indexer --warm`
    /// pass to populate rumoca's semantic-summary cache for common
    /// examples — the workbench's first compile of those classes is
    /// then a cache hit instead of paying the full multi-minute walk.
    pub fn compile_msl_class(
        &mut self,
        qualified: &str,
    ) -> Result<Box<rumoca_compile::compile::DaeCompilationResult>, String> {
        self.compile_loaded(qualified)
    }

    /// Inner helper: heartbeat + session.compile + final timing log.
    /// Both [`Self::compile_str`] (user-edited source) and
    /// [`Self::compile_msl_class`] (already-loaded MSL class) flow
    /// through here so the heartbeat behaviour is identical.
    fn compile_loaded(
        &mut self,
        model_name: &str,
    ) -> Result<Box<rumoca_compile::compile::DaeCompilationResult>, String> {
        let t_total = web_time::Instant::now();

        // Heartbeat: rumoca's compile pipeline is opaque from outside
        // and can take minutes on cold caches with MSL-heavy models
        // (parol Debug::fmt overhead — see ../rumoca/docs/design-notes/
        // perf-parol-trace-overhead.md). Without a periodic log line,
        // the user sees nothing for the entire duration and reasonably
        // assumes the worker hung. Spawn a tiny thread that emits an
        // INFO log every 5s while the synchronous compile is in
        // flight; signal it to stop on return.
        //
        // Wasm note: `std::thread::spawn` panics on wasm32-unknown-unknown
        // (single-threaded target). The compile already runs on the main
        // task there via the inline worker, so the user sees the freeze
        // anyway — heartbeat would just be cosmetic. Skip it.
        use std::sync::atomic::{AtomicBool, Ordering};
        let still_compiling = std::sync::Arc::new(AtomicBool::new(true));
        #[cfg(not(target_arch = "wasm32"))]
        {
            let stopper = std::sync::Arc::clone(&still_compiling);
            let model_for_thread = model_name.to_string();
            // Spawn detached — we deliberately do NOT join after compile
            // returns. The heartbeat sleeps in 5-second chunks; joining
            // would block the worker for up to a full tick (5 s) on EVERY
            // compile, even fast cache-hit ones. The workbench's
            // is_compiling flag would then stay set for that whole window,
            // and the Step dispatcher would idle visibly. Letting the
            // JoinHandle drop detaches the thread; it self-exits within
            // 5 s of `stopper=false` with at most one stray "still
            // compiling +N s" log line if the timing aligns badly.
            let _ = std::thread::spawn(move || {
                let started = web_time::Instant::now();
                let tick = std::time::Duration::from_secs(5);
                loop {
                    std::thread::sleep(tick);
                    if !stopper.load(Ordering::Relaxed) {
                        return;
                    }
                    log::info!(
                        "[ModelicaCompiler] still compiling `{}` (+{:.0}s)",
                        model_for_thread,
                        started.elapsed().as_secs_f64()
                    );
                }
            });
        }

        let mut result = self
            .session
            .compile_model_dae_strict_reachable_uncached_with_recovery(model_name);

        // Demand-driven libraries. If the compile failed, ask rumoca *why*: only an
        // unresolved-reference code (`ER002`/`ER003`) — a class a library could supply
        // — triggers an install. A library-free model (Balloon) succeeds first pass and
        // never reaches here; a genuinely broken model (syntax error, typo) fails with a
        // different code and is returned as-is, paying no install.
        //
        // CHEAPEST FIRST, and each gated independently so a model needing only one does
        // not pay the other. `LunCo` is a handful of embedded docs, so any unresolved
        // reference earns it. MSL is a 316 MB bundle, so it is reached for ONLY if the
        // target STILL has unresolved refs after `LunCo` — otherwise every EPS model
        // (which references `LunCo` but not MSL) would drag MSL in for nothing.
        if result.is_err() && !self.lunco_installed && self.target_has_unresolved_refs(model_name) {
            if self.ensure_lunco_installed() {
                log::info!("[ModelicaCompiler] `{model_name}` had unresolved refs — installed LunCo, retrying");
                result = self
                    .session
                    .compile_model_dae_strict_reachable_uncached_with_recovery(model_name);
            }
        }
        if result.is_err()
            && !self.msl_installed
            && msl_is_available()
            && self.target_has_unresolved_refs(model_name)
            && self.ensure_msl_installed()
        {
            log::info!("[ModelicaCompiler] `{model_name}` still had unresolved refs — installed MSL, retrying");
            result = self
                .session
                .compile_model_dae_strict_reachable_uncached_with_recovery(model_name);
        }

        still_compiling.store(false, Ordering::Relaxed);
        // No `join` — see spawn comment above. The thread is detached
        // and will exit on its own within one tick.

        log::info!(
            "[ModelicaCompiler] compile `{}` finished in {:.2}s ({})",
            model_name,
            t_total.elapsed().as_secs_f64(),
            if result.is_ok() { "OK" } else { "ERR" },
        );
        result
    }

    /// Does compiling `model_name` against the *current* session fail with
    /// an unresolved type/component reference (rumoca diagnostic code
    /// `ER002`)? This is the demand-driven gate's oracle: it distinguishes
    /// "needs a library" from "genuinely broken" using rumoca's **structured
    /// error code**, not message-string matching. Runs the report-returning
    /// strict pass, which reuses the session's already-resolved state — the
    /// cheap second pass rumoca documents — and is only ever taken on the
    /// compile error path before MSL has been installed.
    fn target_has_unresolved_refs(&mut self, model_name: &str) -> bool {
        let report = self
            .session
            .compile_model_strict_reachable_uncached_with_recovery(model_name);
        // ER002 = undefined reference (a type/component use rumoca can't
        // resolve); ER003 = base class not found (an `extends Modelica.…`
        // whose base is missing). Both mean "a symbol a library could
        // provide is absent" — exactly the demand-driven load trigger. A
        // model that `extends` an MSL class hits ER003, not ER002, so the
        // gate must accept both or `extends`-only models never load MSL.
        report
            .failures
            .iter()
            .any(|f| matches!(f.error_code.as_deref(), Some("ER002") | Some("ER003")))
    }

    /// Access the underlying `rumoca_compile::Session` — used by a
    /// test helper that needs to inspect loaded source roots.
    #[cfg(test)]
    pub fn session(&self) -> &Session {
        &self.session
    }

    /// Merge a Modelica source root into the live session so
    /// subsequent compiles can resolve its types. Used by the
    /// `LoadSourceRoot` worker command (`source_roots` lazy-load
    /// pipeline) — main thread sends this command before a Compile
    /// that depends on the library. Idempotent: rumoca dedups by
    /// `id`, so re-issuing for an already-loaded root is cheap.
    ///
    /// Blocks the worker thread for the duration of the parse:
    /// MSL warm-bundle ~1–3 s; cold parse 10–60 s. Other queued
    /// commands wait behind it.
    pub fn load_source_root(
        &mut self,
        id: &str,
        root_dir: &std::path::Path,
    ) -> rumoca_compile::compile::SourceRootLoadReport {
        // MSL fast path: a pre-parsed bundle (`parsed-msl.bin`,
        // ~316 MB) sits next to the MSL source tree, produced by
        // `msl_indexer`. Installing it via
        // `Session::replace_parsed_source_set` takes ~1–3 s vs the
        // 30+ s cold-parse path of `load_source_root_tolerant` over
        // 2847 .mo files. The same fast path already runs inside
        // [`ModelicaCompiler::new`]'s `preload_from_global`; we
        // duplicate it here so the lazy `LoadSourceRoot` worker
        // command also benefits when MSL is loaded on-demand
        // (i.e. after the compiler was created empty for non-MSL
        // models like Balloon).
        if id == "Modelica" {
            // Shared fast path with `preload_from_global`: install the
            // memoized pre-parsed bundle (native streams `parsed-msl.bin`
            // once per process; wasm uses the decoder slot) rather than
            // re-reading + re-deserialising the ~316 MB file here.
            if let Some((total, inserted)) = Self::install_parsed_msl(&mut self.session, id) {
                log::info!(
                    "[ModelicaCompiler] installed pre-parsed MSL bundle \
                     ({} of {} docs)",
                    inserted, total,
                );
                return rumoca_compile::compile::SourceRootLoadReport {
                    source_set_id: id.to_string(),
                    source_root_path: lunco_assets::msl_dir()
                        .join("parsed-msl.bin")
                        .display()
                        .to_string(),
                    parsed_file_count: total,
                    inserted_file_count: inserted,
                    cache_status: None,
                    cache_key: None,
                    cache_file: None,
                    diagnostics: Vec::new(),
                };
            }
        }
        self.session.load_source_root_tolerant(
            id,
            rumoca_compile::compile::SourceRootKind::DurableExternal,
            root_dir,
            None,
        )
    }

    /// Merge an in-memory source root (e.g. a bundled `.mo` file or
    /// a single workspace file) into the live session. Same
    /// idempotency + blocking semantics as
    /// [`Self::load_source_root`], but bytes are passed inline so
    /// callers without a real on-disk path can still install
    /// sources.
    ///
    /// `label` shows up in diagnostics as the "source root path"
    /// (rumoca convention: `"in-memory:<id>"`). `files` is a list
    /// of `(uri, source)` pairs; each `uri` is the filename rumoca
    /// will report errors against.
    pub fn load_source_root_in_memory(
        &mut self,
        id: &str,
        label: &str,
        files: Vec<(String, String)>,
    ) -> rumoca_compile::compile::SourceRootLoadReport {
        let file_count = files.len();
        let mut inserted = 0;
        for (uri, text) in &files {
            if self.session.add_document(uri, text).is_ok() {
                inserted += 1;
            }
        }
        rumoca_compile::compile::SourceRootLoadReport {
            source_set_id: id.to_string(),
            source_root_path: label.to_string(),
            parsed_file_count: file_count,
            inserted_file_count: inserted,
            cache_status: None,
            cache_key: None,
            cache_file: None,
            diagnostics: Vec::new(),
        }
    }
}

/// Convert a rumoca [`StrictCompileReport`] into the Diagnostics
/// panel's [`Diagnostic`](lunco_doc::Diagnostic) form.
///
/// A failure becomes *located* (click-to-source) only when its primary
/// label points at `user_uri` — diagnostics rooted in MSL / sibling
/// library files keep their message (suffixed with the originating
/// file name) but no line/column, so clicking never jumps the editor
/// to a file it isn't showing. Each message is prefixed with rumoca's
/// diagnostic code when present (`ED008`, `EI001`, …) so the panel
/// reads like a real compiler's output.
///
/// [`StrictCompileReport`]: rumoca_compile::compile::StrictCompileReport
fn diagnostics_from_strict_report(
    report: &rumoca_compile::compile::StrictCompileReport,
    user_uri: &str,
) -> Vec<lunco_doc::Diagnostic> {
    use lunco_doc::Diagnostic;
    report
        .failures
        .iter()
        .map(|f| {
            let message = match &f.error_code {
                Some(code) if !code.is_empty() => format!("[{code}] {}", f.error),
                _ => f.error.clone(),
            };
            // Resolve the primary span against the report's source map.
            // `source_map` is `None` only on malformed reports; spans in
            // dummy/compiler-generated sources resolve to `None` here.
            let resolved = f.primary_label.as_ref().and_then(|label| {
                let sm = report.source_map.as_ref()?;
                let (name, content) = sm.get_source(label.span.source)?;
                Some((name.to_string(), content.to_string(), label.span.start.0))
            });
            match resolved {
                Some((name, content, start)) if name == user_uri => {
                    let (line, column) =
                        crate::document::core::byte_offset_to_line_col(&content, start);
                    Diagnostic::error(message, Some(line), Some(column))
                }
                // Diagnostic in another file — name it so the user knows
                // where it came from, but leave it unlocated.
                Some((name, _, _)) => {
                    Diagnostic::message_only(format!("{message}  (in {name})"))
                }
                None => Diagnostic::message_only(message),
            }
        })
        .collect()
}

/// Convert a rumoca-sim runtime/lowering failure into located diagnostics for
/// the Diagnostics panel — the runtime counterpart of
/// [`diagnostics_from_strict_report`].
///
/// [`rumoca_sim::SimulationDiagnosticError`] is structured: it
/// carries a stable diagnostic code and (for the `SolveLowering` variant) a
/// source span via `source_span()`. We surface the code-prefixed `Display`
/// message and, when a span is present and lands inside the user's model
/// source, a clickable `(line, column)`. `Solver` runtime blow-ups have no
/// span and degrade to a single message-only row.
///
/// `source` must be the text those byte offsets index into — i.e. the source
/// that was compiled to the DAE. The compiled text is the *stripped* source
/// (`strip_input_defaults`, applied at the `seat_user_source` chokepoint), but
/// the strip BLANKS the binding bytes in place instead of deleting them, so it
/// is length-preserving: every offset in the stripped source indexes the same
/// character in the caller's raw buffer, and click-to-source stays correct.
fn diagnostics_from_sim_error(
    err: &rumoca_sim::SimulationDiagnosticError,
    source: &str,
) -> Vec<lunco_doc::Diagnostic> {
    use lunco_doc::Diagnostic;
    let message = format!("[{}] {err}", err.diagnostic_code());
    match err.source_span() {
        // `start.0` is a 0-based UTF-8 byte offset; only trust it when it
        // lands inside the buffer we're resolving against (guards against a
        // span rooted in a library/compiler-generated source).
        Some(span) if span.start.0 <= source.len() => {
            let (line, column) =
                crate::document::core::byte_offset_to_line_col(source, span.start.0);
            vec![Diagnostic::error(message, Some(line), Some(column))]
        }
        _ => vec![Diagnostic::message_only(message)],
    }
}


