/// One-call workbench bundle — the canonical "lunica IDE" wiring.
///
/// Everything the Modelica workbench needs to behave **identically**
/// whether it runs standalone (`lunica`, native or wasm) or embedded as a
/// host app's workspace (sandbox's "Design" tab) lives here, so there is
/// exactly one place to fix and no second site to drift against.
///
/// Bundles:
///   - [`lunco_workbench::WorkbenchPlugin`] (added only if the host
///     hasn't already — sandbox adds it early in its own chain);
///   - the passed [`ModelicaUiConfig`];
///   - [`ModelicaPlugin`] (panels + core + viz);
///   - **wasm-only**: the JS clipboard bridge ([`ui::wasm_clipboard`]),
///     localStorage autosave ([`ui::wasm_autosave`]), and the off-thread
///     compile worker ([`worker_transport::install_worker`]).
///
/// History: before this existed, `lunica_web.rs` and `sandbox.rs` each
/// re-wired the workbench by hand and drifted — the sandbox embed shipped
/// with no clipboard bridge (paste broken) and no autosave (reload lost
/// docs), while neither standalone web path nor sandbox stayed in sync on
/// the worker/UI-config knobs. Host apps select the embed flavour via the
/// `config` field:
///
/// ```ignore
/// app.add_plugins(lunco_modelica::ModelicaWorkbenchPlugin {
///     config: lunco_modelica::ModelicaUiConfig {
///         include_help_overlay: false,
///         ..Default::default()
///     },
/// });
/// ```
///
/// App-global frame pacing (`WinitSettings`, `Time<Virtual>` cap,
/// `ClearColor`) is deliberately **not** owned here — Bevy always inserts
/// defaults for those resources so an "insert-if-absent" guard can't tell
/// a host's choice from the default. Those stay an app-shell concern.
#[cfg(feature = "ui")]
#[derive(Default)]
pub struct ModelicaWorkbenchPlugin {
    /// UI knobs (help overlay, welcome panel). Defaults to the full
    /// lunica onboarding experience; embeds typically disable the
    /// help overlay.
    pub config: ModelicaUiConfig,
}

#[cfg(feature = "ui")]
impl Plugin for ModelicaWorkbenchPlugin {
    fn build(&self, app: &mut App) {
        if !app.is_plugin_added::<lunco_workbench::WorkbenchPlugin>() {
            app.add_plugins(lunco_workbench::WorkbenchPlugin);
        }
        app.insert_resource(self.config.clone());
        app.add_plugins(ModelicaPlugin);

        // Web-only affordances. Absent on native (native uses the OS
        // clipboard and a real worker thread):
        //  - clipboard: document-level capture-phase copy/cut/paste that
        //    pre-empts bevy_egui's broken async wasm clipboard pipeline.
        //  - autosave: persists Untitled / dirty docs to localStorage so
        //    a page reload doesn't silently lose work.
        #[cfg(target_arch = "wasm32")]
        {
            app.add_plugins(ui::wasm_clipboard::WasmClipboardPlugin);
            app.add_plugins(ui::wasm_autosave::WasmAutosavePlugin);
        }

        // Shareable model links — `CopyShareLink` clipboard command (all
        // platforms) + the wasm boot loader that opens a model carried in
        // the page URL fragment. Cross-platform: the command works on
        // native too (copies a public link).
        app.add_plugins(ui::model_share::ModelSharePlugin);

        // Off-thread Modelica worker. wasm32 has no real threads, so an
        // inline rumoca *compile* (seconds for non-trivial models) freezes the
        // render loop. The worker pool handles that heavy compile/run work — but
        // it is NOT spawned here. Spawning it eagerly at boot is a cold compile
        // of each ~60 MB worker bundle that can take tens of seconds on a weak
        // machine, and the diagram must not wait on it. We only register the
        // worker URL now; `worker_transport::ensure_pool_spawned` spawns the
        // pool the first time a compile/run is requested. Parsing-for-diagram
        // runs on the main thread until a worker is warm (see
        // `dispatch_parse_to_worker`), so the model renders immediately.
        #[cfg(target_arch = "wasm32")]
        worker_transport::register_worker_url("./worker/worker_bootstrap.js");
    }
}

fn build_modelica_core(app: &mut App) {
    let (tx_cmd, rx_cmd) = unbounded();
    let (tx_res, rx_res) = unbounded();

    // Ensure MSL remote management is present (fetching, settings, status).
    // The domain is incomplete without MSL access.
    if !app.is_plugin_added::<msl_remote::MslRemotePlugin>() {
        app.add_plugins(msl_remote::MslRemotePlugin);
    }

    // Register the `.mo` asset loader so domain code can fetch source
    // through `AssetServer::load(...)` instead of `std::fs::read_to_string`.
    // See `docs/architecture/40-asset-io.md`.
    if !app.is_plugin_added::<source_asset::ModelicaSourceAssetPlugin>() {
        app.add_plugins(source_asset::ModelicaSourceAssetPlugin);
    }

    let msl = msl_dir();
    if msl.exists() {
        if let Ok(abs_path) = std::fs::canonicalize(&msl) {
            std::env::set_var("MODELICAPATH", abs_path.to_string_lossy().to_string());
        }
    }

    // Point rumoca at the workspace's shared `.cache/rumoca/`, the
    // same one `modelica_run` and `msl_indexer` use. Without this
    // alignment, the workbench reads XDG default (`~/.cache/rumoca`)
    // while the CLI tools warm `<workspace>/.cache/rumoca` —
    // `msl_indexer --warm` then does NOTHING for first workbench
    // compile, which stretches from ~12 s (warm) to 13+ minutes (cold,
    // observed). Honor an externally-set `RUMOCA_CACHE_DIR` if the
    // caller wants a sandboxed location (CI, tests).
    //
    // Historical note: earlier versions deliberately left this
    // unset to share with `modelica_tester` (which used XDG too).
    // The new `modelica_run` / `msl_indexer` CLI tools standardised
    // on workspace, and the workbench needs to follow suit so a
    // single `--warm` pass benefits every tool.
    #[cfg(not(target_arch = "wasm32"))]
    if std::env::var_os("RUMOCA_CACHE_DIR").is_none() {
        let target = lunco_assets::cache_dir().join("rumoca");
        std::env::set_var("RUMOCA_CACHE_DIR", &target);
        log::info!(
            "[ModelicaPlugin] using rumoca cache at {} (set RUMOCA_CACHE_DIR to override)",
            target.display(),
        );
    }

    #[cfg(not(target_arch = "wasm32"))]
    {
        thread::spawn(move || {
            worker::modelica_worker(rx_cmd, tx_res);
        });
    }

    // On wasm we still hold the inline worker resource as a fallback for
    // pages that haven't loaded the off-thread worker bundle (e.g. local
    // dev where the worker JS file is missing). The Web Worker transport,
    // when wired up by the binary's startup code via
    // `worker_transport::install_worker`, takes precedence — it intercepts
    // commands via its own pump system and ships them to the worker
    // bundle, bypassing the inline path entirely.
    #[cfg(target_arch = "wasm32")]
    {
        app.insert_resource(worker::InlineWorker::default());
    }

    #[cfg(not(target_arch = "wasm32"))]
    app.insert_resource(ModelicaChannels { tx: tx_cmd, rx: rx_res });
    #[cfg(target_arch = "wasm32")]
    {
        // Hand the result-side sender to the worker_transport so the JS
        // `onmessage` callback can deliver decoded results into the same
        // channel the existing `handle_modelica_responses` system drains.
        // Cheap to clone; the original still goes into ModelicaChannels.
        let _ = worker_transport::register_result_sender(tx_res.clone());
        // Same trick on the command side so the JS test bridge can post
        // commands without going through the UI.
        let _ = worker_transport::register_command_sender(tx_cmd.clone());
        app.insert_resource(ModelicaChannels { tx: tx_cmd, rx: rx_res, rx_cmd, tx_res });
    }

    app.init_resource::<crate::state::WorkbenchState>();
    app.init_resource::<SimStreamRegistry>();
    app.init_resource::<SimSampleStream>();
    app.add_message::<ModelicaNotice>();
    app.add_message::<CompileRequested>();

    // ── Document foundation (moved out of the UI plugin so a headless server
    // journals + replicates Modelica edits, not just the GUI) ──────────────
    // The registry (source-of-truth for open `.mo` docs), the egui-free edit
    // funnel (`ModelicaApiEditPlugin` — already UI-free), the A3 journal-wire
    // auto-bridge (`wire_modelica_journal_handle`, reactive/once), and the
    // lifecycle-event drain. `ModelicaUiPlugin` no longer registers these; it
    // adds core first, so the GUI still gets them. Guarded/idempotent so the
    // UI's own `WorkbenchState`/registry init (a legacy safety net) is a no-op.
    app.init_resource::<crate::state::ModelicaDocumentRegistry>();
    if !app.is_plugin_added::<crate::api::ModelicaApiEditPlugin>() {
        app.add_plugins(crate::api::ModelicaApiEditPlugin);
    }
    app.add_systems(Update, crate::doc_ops::drain_document_changes);
    app.add_systems(
        Update,
        crate::doc_ops::wire_modelica_journal_handle
            .run_if(resource_added::<lunco_doc_bevy::JournalResource>),
    );

    // In-app rhai scripting runtime: `RunScenario` (attach + hot-reload a
    // scenario to an entity), the `cmd()`/prelude world-bridge, and the REPL.
    // This is what lets the workbench be DRIVEN by scripts and what the
    // rhai-authored lunica tutorials run on. `LunCoScriptingPlugin` self-supplies
    // the transport-free command core, so this works with NO API server —
    // scripting stays independent of the `lunco-api` feature. Guarded/idempotent.
    #[cfg(feature = "scripting")]
    if !app.is_plugin_added::<lunco_scripting::LunCoScriptingPlugin>() {
        app.add_plugins(lunco_scripting::LunCoScriptingPlugin);
    }

    // Experiments / Fast Run: backend-agnostic registry + this crate's
    // ModelicaRunner binding. UI for the Run buttons and Experiments
    // panel is layered in `ui::experiments_panel` (Step 5+).
    app.add_plugins(lunco_experiments::ExperimentsPlugin);
    app.insert_resource(ModelicaRunnerResource(
        std::sync::Arc::new(experiments_runner::ModelicaRunner::new()),
    ));
    app.init_resource::<experiments_runner::PendingHandles>();
    app.init_resource::<experiments_runner::ExperimentDrafts>();
    app.init_resource::<experiments_runner::ExperimentSources>();
    app.init_resource::<experiments_runner::PlaybackEntities>();
    // Persisted concurrency cap for parallel Fast Runs (settings.json
    // `experiments.max_parallel`; None = platform auto). The apply system
    // pushes it into the runner on startup and on later edits.
    use lunco_settings::AppSettingsExt;
    app.register_settings_section::<experiments_runner::ExperimentSettings>();
    app.add_systems(
        Update,
        (
            experiments_runner::apply_experiment_settings,
            experiments_runner::drain_pending_handles,
        ),
    );

    // Keep the winit event loop pumping at full speed while any sim
    // is active, even when the window loses focus. Otherwise the
    // app's idle `unfocused_mode` (lunica defaults to
    // `reactive_low_power(1s)`) throttles `FixedUpdate` and the
    // Modelica/cosim stepper crawls. Restores the binary's
    // configured idle mode the moment all sims pause/finish.
    app.add_systems(Update, sim_focus_pace);

    app.configure_sets(
        FixedUpdate,
        (ModelicaSet::HandleResponses, ModelicaSet::SpawnRequests).chain(),
    );

    // The model-vs-world clock divergence diagnostic (A3): written every fixed
    // tick by `spawn_modelica_requests`, read by anything that wants to know
    // whether the co-simulation is actually keeping up.
    app.init_resource::<worker::CosimLag>();

    app.register_type::<ModelicaModel>()
        .add_observer(worker::on_remove_modelica)
        .add_systems(FixedUpdate, (
            handle_modelica_responses.in_set(ModelicaSet::HandleResponses),
            spawn_modelica_requests.in_set(ModelicaSet::SpawnRequests),
        ));

    // Global frame-time tracker. Logs every Update tick that exceeds
    // a threshold AND every tick within a 5-second window after the
    // last Modelica edit. The canvas-render-internal SLOW frame
    // instrumentation only caught time spent inside one panel; this
    // catches main-thread blocking in ANY system on the Bevy schedule
    // (other panels, observers, drive_*_cache, etc.).
    app.init_resource::<FrameTimeProbe>();
    app.add_systems(bevy::prelude::First, frame_time_probe_start);
    app.add_systems(bevy::prelude::PreUpdate, frame_time_probe_pre_update_end);
    app.add_systems(bevy::prelude::Update, frame_time_probe_update_end);
    app.add_systems(bevy::prelude::PostUpdate, frame_time_probe_post_update_end);
    app.add_systems(bevy::prelude::Last, frame_time_probe_end);

    #[cfg(target_arch = "wasm32")]
    {
        // Command dispatch on wasm. `pump_commands_to_worker` is the primary
        // drainer: on a queued command it lazily spawns the worker pool and
        // ships the command off-thread. It is ordered BEFORE
        // `inline_worker_process` so that, whenever the pool spawns, the inline
        // fallback sees an empty queue and no-ops. The inline (main-thread)
        // path only does work when `pump_commands_to_worker` declines — i.e.
        // the worker bundle failed to spawn — so a missing/blocked worker still
        // compiles (just on the main thread).
        app.add_systems(
            Update,
            worker_transport::pump_commands_to_worker
                .before(worker::inline_worker_process),
        );
        // Re-seed MSL into workers respawned after a crash, deferred so the
        // ~165 MB bundle isn't re-allocated on the (memory-starved) crash
        // stack. Cheap no-op when nothing is pending.
        app.add_systems(Update, |_world: &mut World| {
            worker_transport::pump_worker_respawns();
        });
        app.add_systems(Update, worker::inline_worker_process);
        app.add_systems(Update, crate::state::update_file_load_result);
        // Drain Web-Worker RunUpdate streams into the runner's
        // RunHandle receivers and clear the runner's busy flag on
        // terminal updates. Cheap when no run is in flight.
        app.add_systems(Update, |_world: &mut World| {
            experiments_runner::pump_wasm_forwarders();
        });
    }
}

/// Keep winit's `unfocused_mode` pegged at `Continuous` while any
/// sim is active (interactive `ModelicaModel { paused: false }` or
/// a non-empty `PendingHandles`), otherwise restore the binary's
/// idle setting. The first invocation snapshots whatever the
/// binary inserted into `WinitSettings` so we can faithfully restore
/// it later — the modelica plugin does not pick the idle policy
/// itself, just respects what the host app chose. No-op when the
/// app is headless (no `WinitSettings` resource).
#[cfg(all(not(target_arch = "wasm32"), feature = "ui"))]
fn sim_focus_pace(
    settings: Option<ResMut<bevy::winit::WinitSettings>>,
    pending: Option<Res<experiments_runner::PendingHandles>>,
    models: Query<&worker::ModelicaModel>,
    keep_awake: Option<Res<lunco_core::KeepAwake>>,
    mut idle: Local<Option<bevy::winit::UpdateMode>>,
) {
    let Some(mut settings) = settings else { return };
    if idle.is_none() {
        *idle = Some(settings.unfocused_mode);
    }
    // This system runs every frame and is the last writer of `unfocused_mode`, so
    // it must honour other subsystems' pacing intent rather than stomp it: a crate
    // that merely sets `WinitSettings` would have its choice reverted here on the
    // next frame. Offline frame recording is the case that matters — an unattended
    // capture has no focused window and is not a Modelica sim, so without this it
    // gets throttled to the reactive timer (~1 s/frame instead of ~50 ms).
    let sim_active = pending.map(|p| !p.0.is_empty()).unwrap_or(false)
        || models.iter().any(|m| !m.paused && m.is_compiled)
        || keep_awake.map(|k| k.wanted()).unwrap_or(false);
    let desired = if sim_active {
        bevy::winit::UpdateMode::Continuous
    } else {
        idle.expect("snapshot set above")
    };
    if settings.unfocused_mode != desired {
        settings.unfocused_mode = desired;
    }
}

// No-op when there's no winit `WinitSettings` to pace: on wasm (the rAF loop
// drives updates regardless of focus) and on a headless `--no-ui` server (no
// winit compiled in). Keeps the symbol present so `add_systems` compiles.
#[cfg(any(target_arch = "wasm32", not(feature = "ui")))]
fn sim_focus_pace() {}

/// Global frame-time probe — start of frame.
fn frame_time_probe_start(mut probe: ResMut<FrameTimeProbe>) {
    let now = web_time::Instant::now();
    probe.frame_start = Some(now);
    probe.pre_update_start = Some(now);
}

fn frame_time_probe_pre_update_end(mut probe: ResMut<FrameTimeProbe>) {
    let now = web_time::Instant::now();
    probe.pre_update_ms = probe
        .pre_update_start
        .map(|t| now.duration_since(t).as_secs_f64() * 1000.0)
        .unwrap_or(0.0);
    probe.update_start = Some(now);
}

fn frame_time_probe_update_end(mut probe: ResMut<FrameTimeProbe>) {
    let now = web_time::Instant::now();
    probe.update_ms = probe
        .update_start
        .map(|t| now.duration_since(t).as_secs_f64() * 1000.0)
        .unwrap_or(0.0);
    probe.post_update_start = Some(now);
}

fn frame_time_probe_post_update_end(mut probe: ResMut<FrameTimeProbe>) {
    let now = web_time::Instant::now();
    probe.post_update_ms = probe
        .post_update_start
        .map(|t| now.duration_since(t).as_secs_f64() * 1000.0)
        .unwrap_or(0.0);
    probe.last_start = Some(now);
}

/// Global frame-time probe — end of frame. Logs the total Bevy
/// schedule time and (if applicable) flags whether we're inside the
/// post-edit window so per-edit hitches anywhere in the app surface.
fn frame_time_probe_end(mut probe: ResMut<FrameTimeProbe>) {
    let now = web_time::Instant::now();
    let last_ms = probe
        .last_start
        .map(|t| now.duration_since(t).as_secs_f64() * 1000.0)
        .unwrap_or(0.0);
    let Some(start) = probe.frame_start else { return };
    let dt_ms = now.duration_since(start).as_secs_f64() * 1000.0;
    let in_window = probe
        .last_edit
        .map(|t| t.elapsed().as_secs_f64() < 5.0)
        .unwrap_or(false);
    // Default fully off. egui idles at ~30 fps and even a long
    // compile / parse can run for seconds, so any unconditional
    // "hard hitch" threshold floods the console during normal use.
    // Enable explicitly with `LUNCO_FRAME_PROBE=1` (any non-empty
    // value) when investigating a specific freeze.
    let probe_enabled = std::env::var_os("LUNCO_FRAME_PROBE").is_some();
    if probe_enabled && (dt_ms > 30.0 || in_window) {
        bevy::log::info!(
            "[FrameTimeProbe] total={dt_ms:.0}ms pre={:.0} update={:.0} post={:.0} last={last_ms:.0}{}",
            probe.pre_update_ms,
            probe.update_ms,
            probe.post_update_ms,
            if in_window { " (post-edit window)" } else { "" }
        );
    }
    probe.frame_start = None;
}

/// Stamp the `last_edit` timestamp from anywhere that mutates a
/// document. The `apply_ops` site in `canvas_diagram` calls this so
/// the post-edit window covers any frame after a user edit.
pub fn frame_time_probe_stamp_edit(world: &mut World) {
    if let Some(mut probe) = world.get_resource_mut::<FrameTimeProbe>() {
        probe.last_edit = Some(web_time::Instant::now());
    }
}

#[derive(Resource, Default)]
pub struct FrameTimeProbe {
    frame_start: Option<web_time::Instant>,
    pre_update_start: Option<web_time::Instant>,
    update_start: Option<web_time::Instant>,
    post_update_start: Option<web_time::Instant>,
    last_start: Option<web_time::Instant>,
    pre_update_ms: f64,
    update_ms: f64,
    post_update_ms: f64,
    last_edit: Option<web_time::Instant>,
}


